def semana():
    Dia=int(input("Digíte el numero del dia de la semana: "))
    if(Dia==1):
        print("lunes")
    elif(Dia==2):
        print("Martes")
    elif(Dia==3):
        print("Miércoles")
    elif(Dia==4):
        print("Jueves")
    elif(Dia==5):
        print("Viernes")
    elif(Dia==6):
        print("Sábado")
    elif(Dia==7):
        print("Domingo")
    else:
        print("Dia no valido")
semana()